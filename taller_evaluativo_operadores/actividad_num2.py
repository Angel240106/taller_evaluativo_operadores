a = 7
b = 3
# a.a != b
resultado_a = a != b
print(f"el resultado del ejercicio a es = {resultado_a}")
# b.a == b
resultado_b = a == b
print(f"el resultado del ejercicio b es = {resultado_b}")
# c.a > b
resultado_c = a > b
print(f"el resultado del ejercicio c es = {resultado_c}")
# d.(a+b) >2
resultado_d = (a+b) > 2
print(f"el resultado del ejercicio d es = {resultado_d}")
# e.(a>7) y (b=3)
resultado_e = (a>7) and (b:=3)
print(f"el resultado del ejercicio e es = {resultado_e}")
# f.(a<7) o (b==3)
resultado_f = (a<7) or (b==3)
print(f"el resultado del ejercicio f es = {resultado_f}")
# g.(a<7) o (b!=3)
resultado_g = (a<7) or (b!=3)
print(f"el resultado del ejercicio g es = {resultado_g}")
